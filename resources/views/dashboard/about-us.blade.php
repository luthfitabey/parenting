            <h3>Penanganan Stunting</h3>
              <p class="fst-italic">
              Upaya penurunan stunting dilakukan melalui dua intervensi gizi, yaitu intervensi spesifik dan intervensi sensitif. Intervensi spesifik merupakan kegiatan yang langsung mengatasi penyebab terjadinya stunting dan umumnya diberikan oleh sektor kesehatan seperti asupan makanan, pencegahan infeksi, status gizi ibu, penyakit menular dan kesehatan lingkungan. Sementara itu, intervensi sensitif merupakan kegiatan yang berhubungan dengan penyebab tidak langsung stunting.  intervensi sensitif memiliki kontribusi sebesar 70 persen sementara intervensi spesifik menyumbang sekitar 30 persennya (Lancet, 2013). Selain dua hal tersebut, diperlukan juga faktor pendukung yang memungkinkan terjadinya penurunan stunting seperti komitmen politik dan kebijakan, keterlibatan pemerintah dan lintas sektor serta kapasitas untuk melaksanakan intervensi yang ada.
