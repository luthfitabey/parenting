            <h3>Penyebab Stunting</h3>
            <img src="assets/img/rembug.jpg" class="img-fluid rounded-4 mb-4" alt="">
            <p class="fw-bold">Penyebab Langsung:</p>
            <ul>
            <li>Kekurangan Gizi Kronis: Kurangnya asupan gizi yang mencukupi, terutama protein, energi, zat besi, dan vitamin A pada anak, dapat menyebabkan stunting. </li>
            <li>Infeksi yang Berulang: Infeksi kronis, seperti diare berulang, infeksi saluran pernapasan, dan infeksi parasit, dapat mengganggu penyerapan nutrisi dan pertumbuhan anak. </li>
            <li>Pola Makan yang Buruk: Konsumsi makanan yang tidak seimbang atau kurang mengandung nutrisi penting juga dapat menyebabkan stunting. </li>
            </ul>
            <p class="fw-bold">Penyebab Tidak Langsung:</p>
            <ul>
            <li>Faktor Ekonomi: Kemiskinan dapat menghambat akses terhadap makanan bergizi dan layanan kesehatan yang diperlukan untuk mencegah stunting. </li>
            <li>Pendidikan dan Pengetahuan: Kurangnya pengetahuan tentang gizi yang baik dan pola makan sehat dapat menyebabkan praktik makan yang tidak sehat. </li>
            <li>Akses Terhadap Layanan Kesehatan: Terbatasnya akses terhadap layanan kesehatan yang berkualitas dapat menghambat deteksi dini dan penanganan stunting. </li>
            <li>Sanitasi dan Kebersihan: Kondisi lingkungan yang tidak bersih dan tidak sehat dapat meningkatkan risiko infeksi dan stunting. </li>
            </ul>
            <p>
            Penyebab stunting seringkali bersifat multifaktorial dan kompleks, melibatkan interaksi antara berbagai faktor tersebut. Upaya pencegahan dan penanggulangan stunting perlu dilakukan secara holistik, termasuk dalam hal gizi, kesehatan, ekonomi, dan lingkungan.
            </p>
